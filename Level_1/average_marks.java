package Level_1;

public class average_marks {
    public static void main(String[] args) {
        double Physics_marks = 95;
        double Chemistry_marks = 96;
        double Maths_marks = 94;
        double average_marks = (Physics_marks + Chemistry_marks + Maths_marks)/3;
        System.out.println("The average marks is " + average_marks);
    }
}
